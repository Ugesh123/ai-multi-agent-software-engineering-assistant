class MockProvider(LLMProvider):
    """Returns deterministic, schema-valid JSON keyed off the agent marker."""

    def __init__(self, canned_overrides: dict[str, str] | None = None) -> None:
        self._responses = {**_CANNED_RESPONSES, **(canned_overrides or {})}

    def _resolve(self, messages: list[LLMMessage]) -> str:
        for message in messages:
            if message.role != "system":
                continue
            match = _MARKER_RE.search(message.content)
            if match and match.group(1) in self._responses:
                return self._responses[match.group(1)]
        return '{"result": "ok"}'

    async def generate(
        self,
        messages: list[LLMMessage],
        *,
        temperature: float | None = None,
    ) -> LLMResponse:
        content = self._resolve(messages)
        return LLMResponse(content=content, model="mock-model", raw={"mock": True})

    async def stream(
        self,
        messages: list[LLMMessage],
        *,
        temperature: float | None = None,
    ) -> AsyncIterator[str]:
        content = self._resolve(messages)
        for chunk in content.split():
            yield chunk + " "

    async def health_check(self) -> bool:
        return True
