"""Deterministic mock LLM provider.

Used exclusively by automated tests (see `app.core.config.LLMProviderKind.MOCK`)
so the full LangGraph workflow can be exercised end-to-end without a live
Ollama server. Each agent embeds an `[AGENT:<ROLE>]` marker in its system
prompt; this provider matches on that marker to return a realistic,
schema-correct canned JSON response for that specific agent step.

Production code must never depend on this class -- it exists purely to
make `OllamaProvider` swappable and the workflow testable in isolation.
"""

